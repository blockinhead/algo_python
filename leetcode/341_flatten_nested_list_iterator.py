class NestedIterator:
    def __init__(self, nestedList: [NestedInteger]):
        self.d = deque()
        self._fill(nestedList)

    def next(self) -> int:
        return self.d.popleft()

    def hasNext(self) -> bool:
        return bool(self.d)

    def _fill(self, nested_list):
        for elem in nested_list:
            if elem.isInteger():
                self.d.append(elem.getInteger())
            else:
                self._fill(elem.getList())

